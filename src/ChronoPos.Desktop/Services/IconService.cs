/// <summary>
/// Service for providing application icons using Iconify icon system
/// Icons are converted from Iconify SVG to WPF Geometry for optimal performance
/// </summary>
public class IconService : IIconService
{
    private readonly Dictionary<string, string> _iconGeometries;

    public IconService()
    {
        _iconGeometries = InitializeIconGeometries();
    }

    public string GetIconGeometry(string iconName)
    {
        return _iconGeometries.GetValueOrDefault(iconName.ToLowerInvariant(), _iconGeometries["unknown"]);
    }

    public string GetIconResourceKey(string iconName)
    {
        return $"Icon{iconName.ToUpperInvariant()}Geometry";
    }

    public void RegisterIconResources()
    {
        var app = System.Windows.Application.Current;
        if (app?.Resources == null) return;

        foreach (var icon in _iconGeometries)
        {
            var resourceKey = GetIconResourceKey(icon.Key);
            try
            {
                var geometry = Geometry.Parse(icon.Value);
                app.Resources[resourceKey] = geometry;
            }
            catch
            {
                // If parsing fails, skip this icon
                continue;
            }
        }
    }

